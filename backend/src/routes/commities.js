/**
 *  @swagger
 *  /api/commities:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Commities]
 *      summary: Get all commities
 *      description: Get all commities
 *      responses:
 *        200:
 *          description: Commities list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Commities"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;
    const payload = await CommitiesDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',
        'name',
        'designation',
        'parentinstitutionorganization',
        'sectorfield',
        'rolestatusincommittee',
        'notified',
        'noofmembers',

        'membersince',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

