/**
 *  @swagger
 *  /api/trainings:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Trainings]
 *      summary: Get all trainings
 *      description: Get all trainings
 *      responses:
 *        200:
 *          description: Trainings list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Trainings"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;
    const payload = await TrainingsDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',
        'titleoftraining',
        'numberofparticipants',
        'majorfocusareaandoutcomes',
        'audiencetype',
        'organizer',
        'nameoforicpersonalattended',
        'detailsoforicpersonnelattended',

        'dateofevent',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

