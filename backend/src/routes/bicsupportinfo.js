/**
 *  @swagger
 *  /api/bicsupportinfo:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Bicsupportinfo]
 *      summary: Get all bicsupportinfo
 *      description: Get all bicsupportinfo
 *      responses:
 *        200:
 *          description: Bicsupportinfo list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Bicsupportinfo"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;
    const payload = await BicsupportinfoDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',
        'levelofsupportfinancialinkind',
        'areafacilitatedthroughsupport',
        'financialinkindsupportextendedforactivity',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

